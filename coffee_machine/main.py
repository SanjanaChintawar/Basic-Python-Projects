MENU = {
    "espresso": {
        "ingredients": {
            "water": 50,
            "coffee": 18,
        },
        "cost": 1.5,
    },
    "latte": {
        "ingredients": {
            "water": 200,
            "milk": 150,
            "coffee": 24,
        },
        "cost": 2.5,
    },
    "cappuccino": {
        "ingredients": {
            "water": 250,
            "milk": 100,
            "coffee": 24,
        },
        "cost": 3.0,
    }
}
money = 0
resources = {
    "water": 300,
    "milk": 200,
    "coffee": 100,
}
ask_again = True


def is_available(order):
    """It confirms that are the resources available in machine to make selected coffee by user"""
    for item in order:
        if order[item] >= resources[item]:
            print(f"Sorry there is not enough {item}.")
            return False
    return True


def process_coins():
    """Its returns the total calculated coins required for coffee given by user"""
    print("Please Insert coins here.")
    total = int(input("how many quarters?: ")) * 0.25
    total += int(input("how many dimes?: ")) * 0.10
    total += int(input("how many nickle?: ")) * 0.05
    total += int(input("how many pennies?: ")) * 0.01
    return total


def transaction_done(transaction, drink_cost):
    """Returns true if the payment is accepted from the user otherwise false"""
    if transaction >= drink_cost:
        change = round(transaction - drink_cost, 2)
        global money
        money += drink_cost
        print(f"Here is ${change} in change")
        return True
    else:
        print("Sorry that's not enough money. Money refunded.")
        return False


def make_coffee(drink_name, ingredients):
    """Deduct the ingredients after using them to make a coffee"""
    for item in ingredients:
        resources[item] -= ingredients[item]
    print(f"Here is your {drink_name} ☕️. Enjoy!")


